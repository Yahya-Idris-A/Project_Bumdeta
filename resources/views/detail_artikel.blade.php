    <p>jogja.jpnn.com, YOGYAKARTA - Dinas Perindustrian dan Perdagangan (Disperindag) Daerah Istimewa Yogyakarta
        (DIY) telah memantau pergerakan harga barang-barang kebutuhan pokok di beberapa pasar di Kota Yogyakarta,
        seperti di Pasar Beringharjo, Pasar Kranggan dan Pasar Demangan.</p>
    <p>Berdasarkan pantauan pada Kamis (6/10), harga cabai merah keriting rata-rata dijual Rp 42.000 per kg atau
        naik Rp 2.000 dari sebelumnya Rp 40.000 per kg. Harga telur ayam saat ini Rp 24.167 per atau naik dari
        sebelumnya Rp 23.500 per kg. Harga beras IR I tetap Rp 10.100 per kg, harga beras IR II Rp 9.933 per kg,
        gula pasir Rp 13.167 per kg, dan minyak goreng curah Rp 12.400 per kg. Beberapa komoditas yang harganya
        terpantau turun adalah cabai rawit hijau turun menjadi Rp 33.667 per kg dari sebelumnya Rp 35.000 per kg dan
        cabai rawit merah menjadi Rp 45.333 per kg dari sebelumnya Rp 51.000 per kg.</p>
    <p>Kepala Disperindag DIY Syam Arjayanti mengatakan mereka akan terus menggelar operasi pasar murah hingga
        Desember 2022 untuk menekan laju inflasiakibat kenaikan harga BBM. "Operasi pasar dan pasar murah kami
        gencarkan terus menerus agar masyarakat bisa mengakses bahan pangan dengan harga yang terjangkau," kata dia,
        Jumat (7/10). Baca Juga: Pemkot Yogyakarta Sudah Mengecek Harga Barang Kebutuhan Pokok, Hasilnya Operasi
        pasar dan pasar murah tersebut, kata Syam, telah dimulai sejak September 2022 di lima kabupaten/kota di DIY.
        Menurutnya, Disperindag DIY menerapkan subsidi ongkos transportasi untuk mendatangkan komoditas pokok dari
        luar daerah sehingga dijual ke pedagang melalui operasi pasar di bawah harga pasaran.</p>
    <p>Syam berharap pedagang dapat menjual kembali ke konsumen dengan harga yang murah. "Ada subsidi transportasi
        untuk distributor sehingga pedagang mendapatkan kulakan dengan harga di bawah dari harga biasanya," ujarnya.
        Sejumlah komoditas yang disediakan di operasi pasar antara lain aneka cabai, telur ayam, bawang merah, serta
        kebutuhan lain yang berpotensi memengaruhi inflasi. "Masing-masing distributor kami berikan subsidi (ongkos
        transportasi) Rp 2.000 per kilogram kebutuhan pokok," kata dia. (antara/jpnn).
